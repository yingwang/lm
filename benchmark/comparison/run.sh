#!/bin/bash
# Cross-language benchmark: LM vs TypeScript vs Python
# Usage: ./benchmark/comparison/run.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(cd "$SCRIPT_DIR/../.." && pwd)"
TASKS_DIR="$SCRIPT_DIR/tasks"
RESULTS_DIR="$SCRIPT_DIR/results"

mkdir -p "$RESULTS_DIR"

export PATH="$HOME/.cargo/bin:$PATH"

echo "Building lmc..."
cargo build --bin lmc --manifest-path "$PROJECT_DIR/Cargo.toml" 2>/dev/null
LMC="$PROJECT_DIR/target/debug/lmc"
echo ""

lm_pass=0; lm_fail=0; lm_total=0
ts_pass=0; ts_fail=0; ts_total=0
py_pass=0; py_fail=0; py_total=0

failed_details=""

printf "%-25s  %-6s  %-6s  %-6s\n" "Task" "LM" "TS" "Python"
printf "%-25s  %-6s  %-6s  %-6s\n" "========================" "======" "======" "======"

for task_dir in "$TASKS_DIR"/*/; do
    task_id=$(basename "$task_dir")
    expected_file="$task_dir/expected.txt"

    [ -f "$expected_file" ] || continue
    expected=$(cat "$expected_file")

    lm_res="—"; ts_res="—"; py_res="—"

    # LM
    src="$task_dir/solution.lm"
    if [ -f "$src" ]; then
        lm_total=$((lm_total + 1))
        actual=$("$LMC" run "$src" 2>/dev/null || echo "__ERROR__")
        if [ "$actual" = "$expected" ]; then
            lm_pass=$((lm_pass + 1)); lm_res="PASS"
        else
            lm_fail=$((lm_fail + 1)); lm_res="FAIL"
            failed_details="$failed_details\n[$task_id/lm]"
        fi
    fi

    # TypeScript
    src="$task_dir/solution.ts"
    if [ -f "$src" ]; then
        ts_total=$((ts_total + 1))
        actual=$(npx tsx "$src" 2>/dev/null || echo "__ERROR__")
        if [ "$actual" = "$expected" ]; then
            ts_pass=$((ts_pass + 1)); ts_res="PASS"
        else
            ts_fail=$((ts_fail + 1)); ts_res="FAIL"
            failed_details="$failed_details\n[$task_id/ts]"
        fi
    fi

    # Python
    src="$task_dir/solution.py"
    if [ -f "$src" ]; then
        py_total=$((py_total + 1))
        actual=$(python3 "$src" 2>/dev/null || echo "__ERROR__")
        if [ "$actual" = "$expected" ]; then
            py_pass=$((py_pass + 1)); py_res="PASS"
        else
            py_fail=$((py_fail + 1)); py_res="FAIL"
            failed_details="$failed_details\n[$task_id/py]"
        fi
    fi

    printf "%-25s  %-6s  %-6s  %-6s\n" "$task_id" "$lm_res" "$ts_res" "$py_res"
done

echo ""
echo "================================"
echo "  Results Summary"
echo "================================"
printf "  %-12s %2d/%2d passed\n" "LM:" "$lm_pass" "$lm_total"
printf "  %-12s %2d/%2d passed\n" "TypeScript:" "$ts_pass" "$ts_total"
printf "  %-12s %2d/%2d passed\n" "Python:" "$py_pass" "$py_total"
echo "================================"

if [ -n "$failed_details" ]; then
    echo ""
    echo "Failed:"
    echo -e "$failed_details"
fi

cat > "$RESULTS_DIR/summary.txt" << EOF
Cross-Language Benchmark Results
================================
Date: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

LM:         ${lm_pass}/${lm_total} passed
TypeScript: ${ts_pass}/${ts_total} passed
Python:     ${py_pass}/${py_total} passed
================================

Note: These solutions were generated by Claude with knowledge of
the expected output. For a valid first-pass correctness benchmark,
solutions should be generated via API with only the task description
(no expected output) provided.
EOF

echo ""
echo "Results saved to $RESULTS_DIR/summary.txt"
